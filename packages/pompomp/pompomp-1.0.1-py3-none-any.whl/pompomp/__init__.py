"""
Initialize global settings for the pompomp CLI,
including pretty tracebacks via Rich.

To disable enhanced tracebacks, use:

- Bash/zsh:
    POMPOMP_TRACEBACK=0

- PowerShell:
    $env:POMPOMP_TRACEBACK = "0"
"""
import os

from rich.traceback import install

if os.getenv("POMPOMP_TRACEBACK", "1") != "0":
    try:
        terminal_width = os.get_terminal_size().columns
    except OSError:
        terminal_width = 120

    install(
        show_locals=True,
        word_wrap=True,
        width=terminal_width,
        extra_lines=7,
    )
