def add(a: int, b: int) -> int:
    """
    Add two integers.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Subtract two integers.
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Multiply two integers.
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Divide two integers.
    """
    return a / b if b != 0 else 0
